pub fn is_solid(tile: u8) -> bool {
    matches!(tile, 2 | 3 | 4)
}

pub fn tile_at(x: f32, y: f32) -> u8 {
    let tx = (x / TILE) as usize;
    let ty = (y / TILE) as usize;
    if tx < MAP_W && ty < MAP_H { MAP[ty][tx] } else { 2 }
}

pub fn can_move(x: f32, y: f32, half: f32) -> bool {
    !is_solid(tile_at(x - half, y - half))
        && !is_solid(tile_at(x + half, y - half))
        && !is_solid(tile_at(x - half, y + half))
        && !is_solid(tile_at(x + half, y + half))
}
